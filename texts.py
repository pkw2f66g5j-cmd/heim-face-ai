def text_symmetry(m, extra, gender):
    za = abs(m["z"]); val, norm = m["value"], m["norm"]
    di, do = extra["sym_inner_eye_pct"], extra["sym_outer_eye_pct"]
    dm, dn = extra["sym_mouth_pct"], extra["sym_nose_pct"]
    if m["score"] >= 9.0:
        title = "Симметрия лица практически идеальна — редкий результат."
    elif m["score"] >= 7.5:
        title = "Симметрия лица высокая — лицо воспринимается гармонично."
    elif m["score"] >= 6.0:
        title = "Симметрия в пределах нормы, есть небольшие отклонения."
    else:
        title = "Симметрия заметно снижена — основная зона потенциала."
    p1 = (f"Метрика оценивает зеркальное совпадение левой и правой сторон лица по четырём "
          f"парам контрольных точек. Твой результат {val:.4f} при норме {norm:.4f} "
          f"(отклонение {za:.2f}σ). Отклонения: внутренние углы глаз ~{di:.1f}%, "
          f"внешние ~{do:.1f}%, уголки рта ~{dm:.1f}%, крылья носа ~{dn:.1f}%.")
    p2 = ("Даже максимальное из этих отклонений находится в пределах, незаметных "
          "невооружённым глазом. Такая симметрия — один из ключевых маркеров генетического "
          "здоровья и подсознательно считывается как признак высокой привлекательности."
          if m["score"] >= 8.0 else
          "Отклонения в допустимых пределах — абсолютная симметрия встречается крайне редко. "
          "Визуально подобные отклонения формируют индивидуальность лица.")
    p3 = ("Thornhill & Gangestad (Facial attractiveness, 1999): симметрия устойчиво "
          "коррелирует с воспринимаемой привлекательностью (r=0.25-0.40) и связана с иммунным статусом.")
    return title, p1, p2, p3, "Симметрия — фундамент первого впечатления, влияет на все зоны лица одновременно."


